// Looping Statements
// For loop
//Syntax:
// for(initialization; condition; increment/decrement){
//     // code to be executed
// }
// WAP to print number from 1 to 10
for (let i = 1; i <= 10; i++) {
    console.log(i);
}

// WAP to print even number which are present between 20 to 30
for (let i = 20; i <= 30; i++) {
    if (i % 2 === 0) {
        console.log(i);
    }
}

